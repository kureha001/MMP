        #　├┐（右足を踏むべき状況の場合）
            #↓
            #○『右足の漕ぐ力』で電力を蓄電する
            #┴
        #　└┐（その他）
            #↓
            #○『左足の漕ぐ力』で電力を蓄電する
        #┴　┴
	#────────────────────────────────────
