def 恐竜ランド():

    print("----------")
    print("恐竜ランド")
    print("----------")

    モータ = (3,15) #モータドライバ:DCモータ
    基盤   = (2,14) #モータドライバ:基盤
    最小   = 1300
    最大   = 3500 
    間隔S  = 50
    間隔E  = -100
    停止   = 0.2

    #基盤の電源をON
    for i in range(2): MMP.PWM_VALUE(基盤[i], 4095)

    #DCモータをスピードアップ
    for val in range(最小,最大,間隔S):
        print("PWM:",val)
        for i in range(2): MMP.PWM_VALUE(モータ[i], val )
        time.sleep(停止)

    time.sleep(1)

    #DCモータをスローダウン
    for val in range(最大,最小,間隔E):
        print("PWM:",val)
        for i in range(2): MMP.PWM_VALUE(モータ[i], val )
        time.sleep(停止)

    #基盤・DCモータの電源をOFF
    for i in range(2):
        MMP.PWM_VALUE(モータ[i], -1)
        MMP.PWM_VALUE(基盤[i], -1)

