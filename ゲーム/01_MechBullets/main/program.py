#┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#┃メイン
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 本体:
    #┌───────────────────────────────────
    #│初期化
    #└───────────────────────────────────
    def __init__(self):
        #┬
        #○Pyxel(画面/音楽)を初期化する
        self.初期化_リソース()
        #│
        #○処理で用いる各種オブジェクトを作成する
        self.初期化_処理セット()
        #│
        #●シーンを『終了画面』から『タイトル画面』に遷移する
        DS.obj.シーン[DS.仕様.シーンID.終了画面].FN遷移.実行()
        #│
        #○フレーム処理を開始する
        pyxel.run(self.C変更.実行, self.C結果.実行)
        #┴

    #┌───────────────────────────────────
    #│リソースの初期化
    #└───────────────────────────────────
    def 初期化_リソース(self):
        #┬
        #○画面を初期化する
        pyxel.init(
                DS.仕様.リソース.画面.幅,
                DS.仕様.リソース.画面.高,
                title = DS.仕様.バージョン)
        pyxel.screen_mode(1)
        #│
        #○リソースファイルを読み込む
        pyxel.load("./resource.pyxres")
        #│
        #●MMPを初期化する
        共通.MMP.初期化(
            DS.仕様.ハード.HC4067の個数,
            DS.仕様.ハード.HC4067のCh数,
            DS.仕様.ハード.HC4067の丸め)
        #│
        #○MMPの音声モジュールを初期化する
        共通.音声.情報.フォルダNo一覧 = DS.仕様.BGM.フォルダNo一覧
        共通.音声.情報.ファイルNo一覧 = DS.仕様.BGM.ファイルNo一覧
        #┴

    #┌───────────────────────────────────
    #│処理セットの初期化
    #└───────────────────────────────────
    def 初期化_処理セット(self):
        #┬
        #≫シーンを生成する
        DS.obj.シーン = {
            DS.仕様.シーンID.タイトル画面 : タイトル画面の生成(),
            DS.仕様.シーンID.プレイ画面   : プレイ画面の生成()  ,
            DS.仕様.シーンID.終了画面     : 終了画面の生成()    }
        #│
        #≫コントローラを生成する
        self.C変更 = 変更コントローラの生成()
        self.C結果 = 結果コントローラの生成()
        #│
        #≫背景を生成する
        DS.obj.背景 = 背景の生成()
        #┴