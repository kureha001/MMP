#┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#┃メイン
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 本体:
    #┌───────────────────────────────────
    #│初期化
    #└───────────────────────────────────
    def __init__(self   ,
            引数_個体   ):   #① 個体オブジェクト
        #┬
        #○個体オブジェクトのリファレンスを用意する
        self._仕様  = 引数_個体.仕様
        self._情報  = 引数_個体.情報
        #┴

    #┌───────────────────────────────────
    #│機能実行
    #└───────────────────────────────────
    def 実行(self):
        #┬
        #●カモロボの状態を表現する
        self.Fn状態()
        #┴

    #┌───────────────────────────────────
    #│カモロボの状態
    #└───────────────────────────────────
    def Fn状態(self):
        #┬
        #●状態を表現する（立体物）
        #●状態を表現する（画面）
        self.Fn状態_立体()
        self.Fn状態_画面()
        #┴
	#────────────────────────────────────
    def Fn状態_立体(self):
        #※カモロボの機能で実現するためプログラム対応は不要
        pass
	#────────────────────────────────────
    def Fn状態_画面(self):
        #┬
        #○仕様のリファレンスを用意する
        チームNo = self._仕様.チームNo
        #│
        #○背景を表示する
        中心位置 = (42) if チームNo == 0 else (136)
        self.Fn状態_画面_背景(中心位置,65)
        #│
        #○状態を確認する
        if self._情報.命中タイマ <= 0: return
        #│＼（命中タイマーが時間切れの場合）
        #│ ↓
        #│ ▼処理を中断する
        #│
        #○命中タイマーを更新する
        self._情報.命中タイマ -= 1
        #│
        #◇┐撃たれたカモロボを描画する
        if チームNo == 0: pyxel.blt( 2,25, 0,  0,0,  79,79, 0)
        else            : pyxel.blt(98,25, 0, 80,0, 159,79, 0)
        #┴
	#────────────────────────────────────
    def Fn状態_画面_背景(self,cx, cy):
        #┬
        #○描画条件を用意する
        radius = 40     # 外円の半径
        color_main = 7  # メインの色（白）
        color_aux = 5   # 補助線の色（グレー）
        #│
        #○外円を描く
        pyxel.circb(cx, cy, radius, color_main)
        #│
        #○クロスラインを描く
        pyxel.line(cx - radius, cy, cx + radius, cy, color_main)  # 横線
        pyxel.line(cx, cy - radius, cx, cy + radius, color_main)  # 縦線
        #│
        #○中心の点を描く
        pyxel.circ(cx, cy, 2, color_main)
        #│
        #○補助目盛（円形の目印）を描く
        for r in range(10, radius, 10):  # 半径10, 20, 30
            pyxel.circb(cx, cy, r, color_aux)
        #│
        #○補助目盛（斜めライン）を描く
        for dx, dy in [(-10, -10), (10, -10), (-10, 10), (10, 10)]:
            pyxel.line(cx + dx, cy + dy, cx + dx//2, cy + dy//2, color_main)
        #┴