#┃メイン
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 描画クラス:
    #┌───────────────────────────────────
    #│初期化
    #└───────────────────────────────────
    def __init__(self   ,
            引数_個体   ):   #① 個体オブジェクト
        #┬
        #〇個体オブジェクトのリファレンスを用意する
        self._情報  = 引数_個体.情報
        #┴

    #┌───────────────────────────────────
    #│機能実行
    #└───────────────────────────────────
    def 実行(self):
        #┬
        #選手の姿勢を描画する
        姿勢 =  self._情報.姿勢
        if   姿勢 == 0: pyxel.blt( 80,20, 0,  0,0, 23,119, 0)
        elif 姿勢 == 1: pyxel.blt( 60,20, 0, 24,0, 39,119, 0)
        else          : pyxel.blt( 60,20, 0, 48,0, 39,119, 0)
        #┴