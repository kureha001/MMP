#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 入出力():

    MMP     = None  # MMPオブジェクト

	#────────────────────────────────────    
    def MMP_初期化():
        #┬
        #●MMPを実体化する。
        入出力.MMP = mmp()
        #│
        #●MMPを接続する
        入出力.MMP.通信接続_自動()
        #│
        #●アナログ入力を設定する
        入出力.MMP.アナログ設定(
            4,  # 使用するHC4067の個数(1～4)
            1,  # 使用するHC4067のPin数(1～16)
            100 # アナログ値の丸め(この数値以下は切り捨て)
            )
        #┴
	#────────────────────────────────────    
    def 停止():
        入出力.MMP.PWM_VALUE(DS.ハード.装置No_屈伸, DS.ハード.モータ最小)
        入出力.MMP.PWM_VALUE(DS.ハード.装置No_着地, DS.ハード.サーボ最大)
