#┃ＭＭＰによる演奏
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class BGM_DFP():

    # シーン別のMP3ファイル再生時間
    シーン = {1:(15,168), 2:(15,240), 3:(15,240) }

	#────────────────────────────────────
    # シーンに合ったBGMを演奏する
	#────────────────────────────────────
    def 自動選択():
        シーン  = DS.情報.シーン
        if   シーン == シーンID.タイトル画面: BGM_DFP.演奏(1)
        elif シーン == シーンID.プレイ画面  : BGM_DFP.演奏(2)
        elif シーン == シーンID.終了画面    : BGM_DFP.演奏(3)
	#────────────────────────────────────
    # シーン用(フォルダNo.1)の指定トラックを演奏する
	#────────────────────────────────────
    def 演奏(引数_ファイル番号):
        音量 = BGM_DFP.シーン[引数_ファイル番号][0]
        時間 = BGM_DFP.シーン[引数_ファイル番号][1]
        DS.情報.再生時間 = 時間
        # 停止➡WAIT➡音量➡再生の順に実行すること(フリーズする為)
        入出力.MMP.DFP_Stop(1)
        time.sleep(0.2)
        入出力.MMP.DFP_Volume(1,音量)
        入出力.MMP.DFP_PlayFolderTrack(1,1,引数_ファイル番号)
	#────────────────────────────────────
    # 指定フォルダ・トラックを演奏する
	#────────────────────────────────────
    def 指定曲(引数_フォルダ番号, 引数_ファイル番号, 引数_音量=20):
        # 停止➡WAIT➡音量➡再生の順に実行すること(フリーズする為)
        入出力.MMP.DFP_Stop(1)
        time.sleep(0.2)
        入出力.MMP.DFP_Volume(1,引数_音量)
        入出力.MMP.DFP_PlayFolderTrack(1,引数_フォルダ番号,引数_ファイル番号)
	#────────────────────────────────────
    # 繰返し再生する
	#────────────────────────────────────
    def 繰返し再生():
        #┬
        #○BGMを繰返し条件を確認する
        フレーム判定 = (pyxel.frame_count % 30 != 0)
        再生中要否   = (DS.情報.再生時間 is None)
        if フレーム判定 or 再生中要否: return
        #│＼（フレーム処理タイミングでないか、BGMの再生が不要の場合）
        #│ ↓
        #│ ▼処理を中断する
        #│
        #○再生時間カウンタをカウントダウンする
        DS.情報.再生時間 -= 1
        if DS.情報.再生時間 > 0: return
        #│＼（再生時間が終了していない場合）
        #│ ↓
        #│ ▼処理を中断する
        #│
        #○再生時間カウンタを初期化する
        DS.情報.再生時間 = None
        #│
        #●ＢＧＭを再生する
        BGM_DFP.自動選択()
        #┴

