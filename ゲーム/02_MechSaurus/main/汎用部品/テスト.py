def サーボ(
        モータ      ,   # サーボモータのアドレス
        最小        ,   # 110～
        最大        ,   # ～660
        増分 = 2    ,   # 増分
        間隔 = 0.02 ,   # 間隔
        待ち = 1    ,    # 待ち時間(秒)
        中央補正 = -18  
        ):

    中央 = int((最大 + 最小) / 2) + 中央補正

    for i in モータ:MMP.PWM_VALUE(i,中央)
    time.sleep(待ち)

    for val in range(中央,最大,増分):
        for i in モータ:MMP.PWM_VALUE(i,val)
#        print(val)
        time.sleep(間隔)
    time.sleep(待ち)

    for val in range(最大,最小,-増分):
        for i in モータ:MMP.PWM_VALUE(i,val)
        time.sleep(間隔)
#        print(val)
    time.sleep(待ち)

    for val in range(最小,中央,増分):
        for i in モータ:MMP.PWM_VALUE(i,val)
        time.sleep(間隔)


